package firstjava;

public class ForSample2 {
    public static void main(String[] args) {
        for (int i = 0; i < 36; i += 5) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int i = 20; i > -1; i -= 3) {
            System.out.print(i + " ");
        }
    }
}
